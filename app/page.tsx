function downloadAshtangaPDF() {
  const doc = new jsPDF({ orientation: "portrait", unit: "mm", format: "a4" });
  const margin = 20;
  let y = 20;
  doc.setFont("helvetica", "bold"); doc.setFontSize(20);
  doc.text("Ashtanga Primary Series", margin, y); y += 6;
  doc.setFont("helvetica", "normal"); doc.setFontSize(10); doc.setTextColor(120);
  doc.text("Traditional sequence as taught by Sri K. Pattabhi Jois", margin, y); y += 10;
  doc.setDrawColor(200); doc.line(margin, y, 210 - margin, y); y += 8;
  ashtangaPrimary.forEach((section) => {
    if (y > 260) { doc.addPage(); y = 20; }
    doc.setFont("helvetica", "bold"); doc.setFontSize(11); doc.setTextColor(80, 60, 180);
    doc.text(section.phase.toUpperCase(), margin, y); y += 6;
    doc.setFont("helvetica", "normal"); doc.setFontSize(10); doc.setTextColor(40);
    section.poses.forEach((pose, i) => {
      if (y > 270) { doc.addPage(); y = 20; }
      doc.text(`${i + 1}. ${pose}`, margin + 4, y); y += 5.5;
    });
    y += 4;
  });
  doc.setFontSize(8); doc.setTextColor(160);
  doc.text("Generated by Vinyasaurus Flex", margin, 290);
  doc.save("ashtanga-primary-series.pdf");
}

