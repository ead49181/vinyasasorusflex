  const [sequence, setSequence] = useState<any>(null);
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState("");

  async function generateSequence() {
    if (!peak) { setError("Please enter a peak pose."); return; }
    setError("");
    setLoading(true);
    setSequence(null);
    try {
      const res = await fetch("/api/generate-sequence", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ peak, duration, style }),
      });
      const data = await res.json();
      let clean = data.sequence.replace(/```json|```/g, "").trim();
      const firstBrace = clean.indexOf("{");
      const lastBrace = clean.lastIndexOf("}");
      clean = clean.slice(firstBrace, lastBrace + 1);
      const parsed = JSON.parse(clean);
      try {
        const yogaRes = await fetch("https://yoga-api-nzy4.onrender.com/v1/poses");
        const yogaPoses = await yogaRes.json();
        parsed.phases.forEach((phase: any) => {
          phase.poses.forEach((pose: any) => {
            const poseName = pose.name?.toLowerCase().trim();
            const match = yogaPoses.find((p: any) => {
              const apiName = p.english_name?.toLowerCase().trim();
              return (
                apiName === poseName ||
                (apiName && poseName && apiName.length > 6 && poseName.includes(apiName)) ||
                (apiName && poseName && poseName.length > 6 && apiName.includes(poseName))
              );
            });
            if (match) pose.image = match.url_png;
          });
        });
      } catch {
        console.log("Could not load pose images");
      }
      setSequence(parsed);
    } catch (e) {
      console.error(e);
      setError("Something went wrong. Please try again.");
    }
    setLoading(false);
  }

